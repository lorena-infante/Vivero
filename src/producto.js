class Producto {
    constructor(nombre, precio, altura,
        img, descripcion, cuidados, iluminacion, stock, temporada, id) {
        this.nombre = nombre;
        this.precio = precio;
        this.altura = altura;
        this.img = img;
        this.descripcion = descripcion;
        this.cuidados = cuidados;
        this.iluminacion = iluminacion;
        this.stock = stock;
        this.temporada = temporada;
        this.id = id;
    }
}